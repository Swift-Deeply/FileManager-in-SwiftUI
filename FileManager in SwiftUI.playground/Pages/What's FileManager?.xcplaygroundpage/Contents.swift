# What's FileManager?
 FileManager enables us to create and edit data for JSON or plist file types, and to use this data in our apps.
 
 FileManager is very similar to CoreData, but I can say that FileManager is more understandable than CoreData. In short, while you can create more complex data structures with CoreData, you can create simpler data structures in FileManager faster way. So, FileManager is great for a small indie application.
